package Java1课本示例代码.Chapter10.Example10_13;

import java.io.Serializable;

public class TV implements Serializable {
    String name;
    int price;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
